package MODAL;
public class Admin {
    private String Name;
    private String Email;
    private String Pass;
    public String getName() {
        return Name;
    }
    public void setName(String Name) {
        this.Name = Name;
    }
    public String getEmail() {
        return Email;
    }
    public void setEmail(String Email) {
        this.Email = Email;
    }
    public String getPass() {
        return Pass;
    }
    public void setPass(String Pass) {
        this.Pass = Pass;
    }
}
